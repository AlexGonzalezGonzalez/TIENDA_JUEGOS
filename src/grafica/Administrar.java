    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            DefaultTableModel modelo = (DefaultTableModel) this.jTable1.getModel();
            int filas = this.jTable1.getRowCount();
            for (int i = 0; filas > i; i++) {
                this.modeloTabla.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
        try {
            String line = "";
            // String[] sep, sep2;
            String[] sep = new String[7];

            final BufferedReader reader;

            reader = new BufferedReader(new FileReader("JUEGOS.txt"));

            while ((line = reader.readLine()) != null) {
                Object[] linea = new Object[4];
                for (int i = 0; i < sep.length; i++) {
                    sep = line.split(",");
                    for (int j = 0; j <= i; j++) {
                        if (i == 0) {
                            linea[0] = sep[0];
                        }
                        if (i == 1) {
                            linea[1] = sep[1];
                        }
                        if (i == 2) {
                            linea[2] = sep[2];
                        }
                        if (i == 3) {
                            linea[3] = sep[3];
                        }

                    }

                }

                modeloTabla.addRow(linea);

            }
            reader.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Administrar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Administrar.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

